package PrimitiveDataTypes;

public class ComparisonOperators {
	public static void main(String args[]) {
		int value1 = 2;
		int value2 = 5;
		if(value1 == value2)
			System.out.println("value1==value2");
		if(value1!=value2)
			System.out.println("value1!=value2");
		if(value1>value2)
			System.out.println("value1>value2");
		if(value1<value2)
			System.out.println("value1<value2");
		if(value1<=value2)
			System.out.println("value1<=value2");
	}
}
